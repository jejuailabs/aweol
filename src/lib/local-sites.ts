/**
 * 우리 고장 유적 — **그 학교 아이들에게만 뜨는 곳.**
 *
 * 기관(`civic-places.ts`)은 종류가 같으면 어디나 같은 이야기를 한다 —
 * 우체국은 어느 동네에 있든 하는 일이 같기 때문이다.
 * **유적은 정반대다.** 애월진성 이야기는 애월에서만 참이다.
 * 그래서 표를 나누고, 각 유적이 **어느 학교 마을에 뜨는지**를 적어둔다.
 *
 * ---
 *
 * **여기 적는 것은 지어내면 안 된다.**
 *
 * 이 프로젝트는 학교 상징(교훈·교화)에서 이미 한 번 이 선을 그었다 —
 * "남의 학교 교화를 그럴듯하게 지어내면 그 학교 아이들이 틀린 걸 자기 학교 것으로
 * 배운다". 유적은 그보다 더하다. 아이가 자기 고장 역사로 외운다.
 *
 * 그래서 규칙을 둔다:
 * 1. **출처를 함께 적는다**(`sources`). 화면에도 보여준다.
 * 2. **출처마다 다른 숫자는 쓰지 않는다.** 애월진성은 둘레가 자료에 따라
 *    549척과 255보로, 높이는 8척과 16척으로 갈린다 — 그런 것은 아예 안 쓴다.
 *    대신 **지금 실제로 남아 있는 것**(북성 80여m, 높이 6m 이상)을 쓴다.
 *    아이가 가서 볼 수 있는 것이 가장 확실하다.
 * 3. 자료가 갈리는 연도(공진학교 1920 / 1921)는 **'1920년대 초' 처럼 폭을 두고 적는다.**
 * 4. 학교가 고칠 수 있어야 한다 — 그 고장을 제일 잘 아는 것은 그 학교다.
 *    (지금은 이 파일이 유일한 자리다. 화면에서 고치는 길은 아직 없다)
 */

export interface LocalSite {
  id: string;
  /** 이 유적이 뜨는 학교들 */
  schoolIds: string[];
  name: string;
  emoji: string;
  /** 학교를 원점으로 한 미터 좌표 (마을 좌표계와 같다) */
  x: number;
  z: number;
  /** 한 줄로 */
  oneLine: string;
  /** 한 장에 한 가지씩 */
  pages: { title: string; body: string }[];
  /** 함께 보는 영상 (유튜브 id). 끝까지 보면 심부름이 끝난다. */
  videoId?: string;
  /** 어디서 온 이야기인가 — 화면에 그대로 보여준다 */
  sources: { label: string; url: string }[];
}

