/**
 * 우리 고장의 유적·명소 — **그 학교 아이들에게만 뜨는 곳.**
 *
 * 기관(`civic-places.ts`)은 종류가 같으면 어디나 같은 이야기를 한다 —
 * 우체국은 어느 동네에 있든 하는 일이 같기 때문이다.
 * **여기는 정반대다.** 애월진성 이야기는 애월에서만 참이다.
 * 그래서 표를 나누고, 각 곳이 **어느 학교 마을에 뜨는지**를 적어둔다.
 *
 * ---
 *
 * **여기 적는 것은 지어내면 안 된다.**
 *
 * 이 프로젝트는 학교 상징(교훈·교화)에서 이미 한 번 이 선을 그었다 —
 * "남의 학교 교화를 그럴듯하게 지어내면 그 학교 아이들이 틀린 걸 자기 학교 것으로
 * 배운다". 유적은 그보다 더하다. 아이가 자기 고장 역사로 외운다.
 *
 * 규칙(자세한 것은 `docs/village-rpg.md`):
 * 1. **출처를 함께 적고**(`sources`) 화면에도 보여준다.
 * 2. **출처마다 다른 숫자는 안 쓴다.** 애월진성 둘레(549척/255보), 구엄
 *    돌염전의 시작(명종 무렵/1702년 강제검 판관)처럼 갈리는 것은 뺀다.
 *    대신 **지금 남아 있는 것**을 쓴다 — 아이가 가서 볼 수 있는 것이 제일 확실하다.
 * 3. 갈리는 연도는 **폭을 두고** 적는다("1920년대 초").
 * 4. **모르면 안 쓴다.** 빈칸이 지어낸 문장보다 낫다.
 *
 * ---
 *
 * **방위와 거리는 모식도다.**
 *
 * 걸어다니는 마을 지도는 학교에서 400m 뿐이라 항파두리도 빌레못동굴도 못 담는다.
 * 그래서 읍 지도를 따로 두고 **방위(8방위)와 대략의 거리**로 그린다.
 * 측량 지도가 아니다 — 아이에게 필요한 건 좌표가 아니라
 * "학교에서 서쪽으로 2km" 이고, 그게 마침 방위를 배우는 일이다.
 * **정확한 자리는 그 고장을 제일 잘 아는 선생님이 여기서 고친다.**
 */

/** 8방위. 읍 지도에서 이 방향으로 놓인다. */
export type Dir = 'N' | 'NE' | 'E' | 'SE' | 'S' | 'SW' | 'W' | 'NW';

/** 어느 칸을 채우는 곳인가 — 조사 수첩이 이걸로 나뉜다 */
export type SiteAxis = 'time' | 'life' | 'nature';

export interface LocalSite {
  id: string;
  /** 이 곳이 뜨는 학교들 */
  schoolIds: string[];
  name: string;
  emoji: string;
  axis: SiteAxis;

  /**
   * 연표에서 어디에 놓이나.
   * `order` 가 작을수록 옛날. 시간축이 아닌 곳은 `null`.
   */
  era: { label: string; order: number } | null;

  /** 학교에서 어느 쪽으로, 대략 몇 km. 학교 안이면 `km: 0`. */
  dir: Dir;
  km: number;

  /**
   * 실제로 가 볼 수 있나.
   * **못 가는 곳도 넣는다** — 빌레못동굴은 아예 못 들어간다.
   * '왜 못 들어가게 하는가' 가 그 자체로 배울 것이다.
   */
  open: boolean;
  /** 못 가는 곳이면 왜 */
  closedWhy?: string;

  /** 한 줄로 */
  oneLine: string;
  /** 한 장에 한 가지씩 */
  pages: { title: string; body: string }[];
  /** 조사하고 나면 얻는 낱말들. 골든벨·OX 문제와 이어진다. */
  keywords: string[];
  /** 함께 보는 영상 (유튜브 id). 끝까지 보면 조사가 끝난다. */
  videoId?: string;
  /** 이 학년쯤부터 — 없으면 누구나 */
  minGrade?: number;
  /** 다루기 조심스러운 주제면 선생님에게 알린다 */
  caution?: string;
  /** 어디서 온 이야기인가 — 화면에 그대로 보여준다 */
  sources: { label: string; url: string }[];
}

